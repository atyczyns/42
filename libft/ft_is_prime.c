int		ft_is_prime(int nb)
{
	int		mult;
	int		i;

	i = 2;
	mult = 0;
	if (nb < 2)
		return (0);
	else
	{
		while (i < nb)
		{
			mult = nb % i;
			++i;
			if (mult == 0)
				return (0);
		}
		return (1);
	}
}
