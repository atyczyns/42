#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	count;

	count = 0;
	while (count < n)
	{
		if (((unsigned char *)s)[0] == (unsigned char)c)
			return ((void *)s);
		s++;
		count++;
	}
	return (NULL);
}
