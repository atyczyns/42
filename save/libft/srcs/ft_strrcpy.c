#include "libft.h"

char	*ft_strrcpy(char *dst, const char *src)
{
	int		i;
	int		j;

	i = ft_strlen(src);
	j = 0;
	dst[i] = '\0';
	while (--i >= 0)
		dst[i] = src[j++];
	return (dst);
}
